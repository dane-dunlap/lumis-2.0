import './globals.css'
import { Inter } from "next/font/google"
import { Button } from "@/components/ui/button"
import { ThemeProvider } from "@/components/theme-provider"
import {
  NavigationMenu,
  NavigationMenuContent,
  NavigationMenuIndicator,
  NavigationMenuItem,
  NavigationMenuLink,
  NavigationMenuList,
  NavigationMenuTrigger,
  NavigationMenuViewport,
} from "@/components/ui/navigation-menu"
import LogoutButton from './components/LogOut'
import { ModeToggle } from '@/components/ui/toggle-mode'
import { createServerComponentClient } from '@supabase/auth-helpers-nextjs'
import { cookies } from 'next/headers'
import Link from 'next/link';







const inter = Inter({ subsets: ['latin'] })

export const metadata = {
  title: 'Create Next App',
  description: 'Generated by create next app',
}

export default async function RootLayout({ children }) {
  const cookieStore = cookies()
  const supabase = createServerComponentClient({ cookies: () => cookieStore })
  
  const {
    data: { session },
  } = await supabase.auth.getSession()
  const user = session?.user

  let { data:email, error, status } = await supabase
  .from('profiles')
  .select(`email`)
  .eq('id', user?.id)
  .single()
  
  return (
    <html lang="en">
      <body className={inter.className}>
      <ThemeProvider
            attribute="class"
            defaultTheme="system"
            enableSystem
            disableTransitionOnChange
        >
        
        <NavigationMenu>
          <NavigationMenuList className="mt-4 ml-2">
            <NavigationMenuItem>
              {!user && <Link href="/signup"><Button>Sign up</Button></Link>}
            </NavigationMenuItem>
            <NavigationMenuItem>
              {user ? <LogoutButton /> : <Link href="/login"><Button variant="secondary" size="default">Login</Button></Link>}
            </NavigationMenuItem>
            <NavigationMenuItem>
              <ModeToggle/>
            </NavigationMenuItem>

    
          </NavigationMenuList>
        </NavigationMenu>
        

        
        {children}
        </ThemeProvider>
        
      </body>
      
    </html>
  )
}

